export default function TechIcon({
  name,
  size = 18,
  className = "",
  style,
  label,
  showLabel = false,
  onMouseEnter,
  onMouseLeave,
}: Props) {
  const tech = detectTech(name);

  if (!tech) {
    const fallbackLabel = label ?? name;

    return (
      <span
        className={`inline-flex items-center gap-3 ${className}`}
        style={style}
      >
        <span className="telem-label" style={{ fontSize: size }}>
          {fallbackLabel}
        </span>
      </span>
    );
  }

  const ariaLabel = label ?? TECH_LABELS[tech];
  const href = TECH_URLS[tech];

  return (
    <a
      href={href}
      target="_blank"
      rel="noopener noreferrer"
      aria-label={ariaLabel}
      title={ariaLabel}
      className={`inline-flex items-center gap-3 ${className}`}
      style={style}
      onMouseEnter={onMouseEnter}
      onMouseLeave={onMouseLeave}
    >
      <svg
        role="img"
        viewBox="0 0 24 24"
        xmlns="http://www.w3.org/2000/svg"
        width={size}
        height={size}
        fill="currentColor"
        aria-hidden="true"
        focusable="false"
        className="flex-shrink-0"
      >
        {ICONS[tech]}
      </svg>

      {showLabel && <span className="telem-label">{ariaLabel}</span>}
    </a>
  );
}
